print("Math Functions")

abs_int = abs(-1)           # absolute value of -1
print(abs_int)

int_to_float = float(100)   # turns any number into floating value
print(int_to_float)

float_to_int = int(1.23)    # convert any number into integer -> can also use print(int(1.23))
print(float_to_int)

print(max(1, 2, -5, 10, 0)) # prints max number
print(min(1, 2, -5, 10, 0)) # prints min number
print(pow(2, 3))            # prints 2 to the power of 3
print(round(51.6))

