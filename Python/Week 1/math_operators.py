# DIVISION (RESULTS ARE IN FLOAT VALUE)

x = 20
y = 5
z = 4

xyz = x / y/ z   # xyz = 20/5/4 = 1.0
x = x / 10       # x = 20/10 = 2/0
y = y / 5        # y = 5/5 = 1.0
z = z /3         #z = 4/3 = 1.3

# FLOOR DIVISION (RESULTS ARE IN INTEGER VALUE)

x = 20
y = 5
z = 4

xyz = x // y // z   # xyz = 20//5//4 = 1
x = x // 10         # x = 20//10 = 2
y = y // 5          # y = 5//5 = 1
z = z //3           #z = 4//3 = 1

# EXPONENTATION

x = 2 ** 0      # x = 2^0 = 1
y = 2 ** 1      # y = 2*1 = 2
z = 2 ** 2.0    # z = 2^2.0 = 4.0

# MODULO - REMAINDER OPERATOR (DIVIDES LEFT OPERAND BY RIGHT OPERAND)

